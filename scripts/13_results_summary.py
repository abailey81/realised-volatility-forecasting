"""
Stage 13 — Single-file results summary.

Consolidates every output table into ``outputs/RESULTS_SUMMARY.md`` so the
full empirical evidence is visible in one place. Intended as the source
the report draft is built from.

Usage:
    python scripts/13_results_summary.py
"""

from __future__ import annotations

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

import numpy as np
import pandas as pd

from src.utils import get_logger, load_config, resolve


def _read_or_none(path: Path) -> pd.DataFrame | None:
    if path.exists():
        try:
            return pd.read_csv(path, index_col=0)
        except Exception:
            return pd.read_csv(path)
    return None


def _section(title: str) -> str:
    return f"\n\n## {title}\n\n"


def _df_to_md(df: pd.DataFrame, decimals: int = 3) -> str:
    if df is None or df.empty:
        return "_(not available)_\n"
    return df.round(decimals).to_markdown() + "\n"


def main() -> int:
    cfg = load_config()
    log = get_logger("results_summary", level=cfg.project.log_level)

    tables_dir = resolve(cfg.paths.outputs_tables)
    lines: list[str] = []
    lines.append("# RV-ML replication — results summary\n")
    lines.append(
        "Generated by `scripts/13_results_summary.py`. All numbers are out-of-sample on the test "
        "set (last ~20% of the 2016-2024 sample by default). Loss ratios are *vs* HAR baseline; "
        "values below 1.000 indicate the model outperforms HAR.\n"
    )

    # ----- HEADLINE: M_ALL MSE loss ratios per horizon -----
    lines.append(_section("Headline — out-of-sample MSE ratios vs HAR (M_ALL)"))
    for h in cfg.forecast.horizons:
        path = tables_dir / f"loss_ratio_h{h}_mse.csv"
        df = _read_or_none(path)
        lines.append(f"### Horizon h={h}\n")
        lines.append(_df_to_md(df))

    # ----- QLIKE robustness -----
    lines.append(_section("QLIKE-loss ratios vs HAR (robustness)"))
    for h in cfg.forecast.horizons:
        path = tables_dir / f"loss_ratio_h{h}_qlike.csv"
        df = _read_or_none(path)
        lines.append(f"### Horizon h={h}\n")
        lines.append(_df_to_md(df))

    # ----- DM tests vs HAR -----
    lines.append(_section("Diebold-Mariano tests vs HAR (one-sided, p-values)"))
    for h in cfg.forecast.horizons:
        path = tables_dir / f"dm_h{h}_mse.csv"
        df = _read_or_none(path)
        lines.append(f"### Horizon h={h}\n")
        lines.append(_df_to_md(df, decimals=4))

    # ----- MCS at three confidence levels -----
    lines.append(_section("Model Confidence Set p-values per stock (Hansen-Lunde-Nason)"))
    for h in cfg.forecast.horizons:
        for a in [25, 10, 5]:
            path = tables_dir / f"mcs_h{h}_mse_a{a:02d}.csv"
            df = _read_or_none(path)
            if df is None:
                continue
            lines.append(f"### Horizon h={h}, α={a/100:.2f}\n")
            lines.append(_df_to_md(df))

    # ----- Mincer-Zarnowitz -----
    lines.append(_section("Mincer-Zarnowitz forecast efficiency"))
    for h in cfg.forecast.horizons:
        path = tables_dir / f"mz_h{h}_mse.csv"
        df = _read_or_none(path)
        if df is None:
            continue
        lines.append(f"### Horizon h={h}\n")
        lines.append(_df_to_md(df, decimals=4))

    # ----- Bootstrap CIs -----
    lines.append(_section("Bootstrap 95% confidence intervals on out-of-sample MSE"))
    for h in cfg.forecast.horizons:
        path = tables_dir / f"bootstrap_ci_h{h}_mse.csv"
        df = _read_or_none(path)
        if df is None:
            continue
        lines.append(f"### Horizon h={h}\n")
        lines.append(_df_to_md(df, decimals=6))

    # ----- Forecast combinations -----
    lines.append(_section("Forecast combinations"))
    for h in cfg.forecast.horizons:
        path = tables_dir / f"combinations_h{h}_mse.csv"
        df = _read_or_none(path)
        if df is None:
            continue
        lines.append(f"### Horizon h={h}\n")
        lines.append(_df_to_md(df))

    # ----- COVID period -----
    lines.append(_section("COVID-period evaluation (train 2016-2019 / test 2020-2024)"))
    for h in cfg.forecast.horizons:
        # Prefer the proper COVID full predictions over the (mostly empty)
        # subset-of-headline-test-set table from stage 9.
        for candidate in (f"covidfull_loss_ratio_h{h}_mse.csv",
                          f"covid_full_loss_ratio_h{h}_mse.csv",
                          f"covid_loss_ratio_h{h}_mse.csv"):
            path = tables_dir / candidate
            if path.exists():
                df = _read_or_none(path)
                lines.append(f"### Horizon h={h} ({candidate})\n")
                lines.append(_df_to_md(df))
                break
        else:
            continue

    # ----- COVID-period MCS survivor sets at α=0.10 -----
    lines.append(_section("COVID-period Model Confidence Set p-values (α from config)"))
    for h in cfg.forecast.horizons:
        for a in [25, 10, 5]:
            path = tables_dir / f"covidfull_mcs_h{h}_mse_a{a:02d}.csv"
            df = _read_or_none(path)
            if df is None:
                continue
            lines.append(f"### Horizon h={h}, α={a/100:.2f}\n")
            lines.append(_df_to_md(df))

    # ----- Realised kernel robustness -----
    lines.append(_section("Realised kernel robustness — HAR family on RK target"))
    df = _read_or_none(tables_dir / "rk_robustness.csv")
    lines.append(_df_to_md(df, decimals=4))

    # ----- Cross-horizon summary -----
    lines.append(_section("Cross-horizon, cross-stock summary (long format)"))
    df = _read_or_none(tables_dir / "summary_cross_horizon_mse.csv")
    lines.append(_df_to_md(df))

    out = resolve(cfg.paths.outputs_tables).parent / "RESULTS_SUMMARY.md"
    out.write_text("".join(lines), encoding="utf-8")
    log.info("Wrote results summary to %s (%d bytes)", out, out.stat().st_size)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
